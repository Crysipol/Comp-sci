#This program draws a flower

import turtle
loaf = turtle.Turtle()
loaf.color("blue")
for i in range(36):
    loaf.forward(100)
    loaf.left(145)
    loaf.forward(10)
    loaf.right(90)
    loaf.forward(10)
    loaf.left(135)
    loaf.forward(100)
